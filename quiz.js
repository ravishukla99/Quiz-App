//decalring necessary variables

//for selecting right ques ans set
var quesAns;

//for calculating attempted ans
var attemptedQues = 0;

//for calculating correct nad incorrect ans
var correctAns = 0;
var incorrectAns = 0;

//for calculating result percentage
var percentage = 0;

//for selected ques category
var quizOptionSelected;

//for selected ques no
var optionNo;

//for calculating timer
var timerCount = 1;

//for storing user name
var userName;

//for storing user email
var userEmail;

//for question ans index
var i = 0;

//for storing interval function
var timer;

var option = document.querySelectorAll(".indexSectionselectCategoryOption");

//function to set user details
function setUserDetails() {
  if (document.getElementById("userName").value != "") {
    userName = document.getElementById("userName").value;
    document.getElementById(
      "quizResNameShow"
    ).innerHTML = `${userName} your quiz result is`;
  }
  if (document.getElementById("userEmail").value.includes("@gmail.com")) {
    userEmail = document.getElementById("userEmail").value;
  }
  console.log(userName, userEmail);
}

//function to make effect in quiz category selection
function optionSelected(opt) {
  optionNo = opt;
  quizOptionSelected = option[opt].innerHTML;
  option[opt].setAttribute("class", "selected");
  for (let i = 0; i < option.length; i++) {
    if (i == opt) {
      continue;
    } else {
      option[i].setAttribute("class", "indexSectionselectCategoryOption");
    }
  }
  console.log(quizOptionSelected);
}

//function to start the quiz
function quizStart() {
  if (quizOptionSelected != null && userName != null && userEmail != null) {
    assignment(i);
    // loadSelectedTypeQuesAns();
    document.getElementById("quizPageHeadSectionCategoryName").innerHTML =
      quizOptionSelected;
    document.getElementById("con1").style.display = "none";
    document.getElementById("con2").style.display = "block";
    timer = setInterval(() => {
      if (timerCount > 10) {
        document.getElementById("quizPageHeadSectionTimer").innerHTML =
          timerCount - 1;
        timerCount = 1;
        changeQue();
      } else {
        if (timerCount < 10) {
          document.getElementById("quizPageHeadSectionTimer").innerHTML =
            "0" + timerCount;
        } else {
          document.getElementById("quizPageHeadSectionTimer").innerHTML =
            timerCount;
        }
        timerCount++;
      }
    }, 1100);
  } else {
    alert("Please Fills And Submit All Details!");
  }
  console.log("started");
}

//assigning first question and ans to the div
function assignment(indx) {
  loadSelectedTypeQuesAns();
  document.getElementById("quesCount").innerHTML =
    indx + 1 + "/" + quesAns.length;
  document.getElementById("quizPageQues").innerHTML = quesAns[indx].ques;
  document.getElementById("ans1").innerHTML = quesAns[indx].ans1;
  document.getElementById("ans2").innerHTML = quesAns[indx].ans2;
  document.getElementById("ans3").innerHTML = quesAns[indx].ans3;
  document.getElementById("ans4").innerHTML = quesAns[indx].ans4;
}

// function to load selected type ques ans
function loadSelectedTypeQuesAns() {
  if (optionNo == 0) {
    quesAns = quesAnsProbability;   
  } else if (optionNo == 1) {
    quesAns = quesAnsPipenCictern;
  } else if (optionNo == 2) {
    quesAns = quesAnsProfitnLoss;
  } else if (optionNo == 3) {
    quesAns = quesAnsProblemsOnAge ;
  }
}

//function for changing questions and answers
function changeQue() {
  i++;
  timerCount = 1;
  for (let i = 1; i <= 4; i++) {
    document.getElementById(`ans${i}`).removeAttribute("class");
    document.getElementById(`ans${i}`).setAttribute("class", "quizPageAn");
  }

  if (i < quesAns.length) {
    assignment(i);
  } else {
    document.getElementById("con2").style.display = "none";
    document.getElementById("con3").style.display = "block";
    document.getElementById("attemptedQues").innerHTML = attemptedQues;
    document.getElementById("correctAns").innerHTML = correctAns;
    if (correctAns != 0) {
      incorrectAns = 10 - correctAns;
    }
    document.getElementById("incorrectAns").innerHTML = incorrectAns;
    percentage = (correctAns / 10) * 100;
    document.getElementById("percentage").innerHTML = percentage;
    clearInterval(timer);
  }
}

//function to make effect of selected answer and disable remaining
function selectAns(ansNo) {
  attemptedQues = attemptedQues + 1;
  correctAns += 1;
  document.getElementById(`ans${ansNo}`).setAttribute("class", "selectedAns");
  for (let i = 1; i <= 4; i++) {
    if (i != ansNo) {
      document
        .getElementById(`ans${i}`)
        .setAttribute("class", "selectedAnsNone");
    } else {
      continue;
    }
  }
}

//function to start the quiz again from result page
function startAg() {
  timerCount = 1;
  attemptedQues = 0;
  correctAns = 0;
  i = 0;
  document.getElementById("con3").style.display = "none";
  quizStart();
}

//function to go to home from result page
function GTHome() {
  clearInterval(timer);
  i = 0;
  document.getElementById("userName").value = null;
  document.getElementById("userEmail").value = null;
  userName = null;
  userEmail = null;
  quizOptionSelected = null;
  for (let i = 0; i < option.length; i++) {
    option[i].setAttribute("class", "indexSectionselectCategoryOption");
  }
  document.getElementById("con3").style.display = "none";
  document.getElementById("con1").style.display = "block";
}
